package org.usfirst.frc.team4750.robot;

import edu.wpi.first.wpilibj.Victor;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	//PWN Ports for Motors
	
	public static final int FRONT_RIGHT_MOTOR = 0;
	public static final int FRONT_LEFT_MOTOR = 1;
	public static final int BACK_RIGHT_MOTOR = 2;
	public static final int BACK_LEFT_MOTOR = 3;
	
	public static final int LEFT_JOYSTICK_USB_PORT = 1;
	public static final int RIGHT_JOYSTICK_USB_PORT = 2;
	public static final int SHOOTER_JOYSTICK_USB_PORT = 3;
	
	
}
